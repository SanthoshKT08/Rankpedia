package Pojo;

public class AddSchool {

	public String name;
	public String type;
	public String board;
	public String institute_type;
	public String phone;
	public String email;
	public String location;
	public String address;
	public String about;
	public Poc poc;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBoard() {
		return board;
	}
	public void setBoard(String board) {
		this.board = board;
	}
	public String getInstitute_type() {
		return institute_type;
	}
	public void setInstitute_type(String institute_type) {
		this.institute_type = institute_type;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAbout() {
		return about;
	}
	public void setAbout(String about) {
		this.about = about;
	}
	public Poc getPoc() {
		return poc;
	}
	public void setPoc(Poc poc) {
		this.poc = poc;
	}



}
